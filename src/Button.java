import java.awt.Graphics2D;
import java.awt.Color;

public class Button {
   
    private int x;
    private int y;
    private int width;
    private int height;
    
    public Button(int x, int y, int width, int height){
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public void paint(Graphics2D g){
        g.setColor();
    }
}
